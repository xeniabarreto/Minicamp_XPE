let hojeFezCalor = false;
let ontemFezCalor = true;

//E - AND
if(hojeFezCalor && ontemFezCalor){
    console.log('Fez calor ontem e hoje.');
}

//OU - OR
if(hojeFezCalor || ontemFezCalor){
    console.log('Fez calor ontem ou hoje.');
}

//NÃO - NOT
if(!hojeFezCalor){
    console.log('Hoje não fez calor.');
}